"""
Numeric Expression or Operator :

Operator : Operation 
+        :  Addition
-        :  Subtraction
*        :  Multiplication
/        :  Division
**       :  Power
%        :  Remainder   

"""
x = 20
y = 5

print("Addition:- ",x+y) #   25
print("Subtraction: ",x-y) #    15 
print("Multiplication: ",x*y) #   100
print("Divison: ",x/y) #  4.0
print(" Power: ",x**y) #  3200000
print("Remainder: ",x%y) #  0 